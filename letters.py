def convert(letter):
    newLetter = copy.deepcopy(letter)
    for i in range(len(letter)):
        for j in range(len(letter[0])):
            if newLetter[i][j] == ' ':
                newLetter[i][j] = 0
            if newLetter[i][j] == '*':
                newLetter[i][j] = 1
    return newLetter

